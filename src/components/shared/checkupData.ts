export function doctorsForGender(p: CheckupPeriod, gender: Gender): string[] {
  const list = [...p.doctors];
  if (gender === "girl" && p.doctorsGirl) list.push(...p.doctorsGirl);
  if (gender === "boy" && p.doctorsBoy) list.push(...p.doctorsBoy);
  if (!gender) {
    if (p.doctorsGirl) list.push(...p.doctorsGirl);
    if (p.doctorsBoy) list.push(...p.doctorsBoy);
  }
  return list;
}
